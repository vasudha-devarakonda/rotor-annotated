def generate(model, tokenizer, input_ids, max_new_tokens=50, temperature=1.0, top_k=None):
    """
    Autoregressive text generation.
    input_ids: [B, T] tensor
    """
    model.eval()
    generated = input_ids

    for _ in range(max_new_tokens):
        logits = model(generated)
        next_token_logits = logits[:, -1, :] / temperature

        if top_k is not None:
            top_k_vals, top_k_idx = torch.topk(next_token_logits, top_k)
            probs = torch.zeros_like(next_token_logits).scatter_(-1, top_k_idx, F.softmax(top_k_vals, dim=-1))
        else:
            probs = F.softmax(next_token_logits, dim=-1)

        next_token = torch.multinomial(probs, num_samples=1)
        generated = torch.cat([generated, next_token], dim=-1)

    return generated

